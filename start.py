    if len(sys.argv) > 1:
        arg = sys.argv[1].lower()
        
        if arg in ['--help', '-h']:
            show_help()
            return
        elif arg == '--version':
            print("InterServer Bot v1.0.0")
            return
        elif arg == '--install':
            install_dependencies()
            return
        elif arg == '--setup':
            setup_bot()
            return
        elif arg == '--check':
            show_startup_info()
            checks_passed = all([
                check_python_version(),
                check_dependencies(),
                check_environment(),
                check_directories(),
                check_permissions()
            ])
            
            if checks_passed:
                print("‚úÖ –í—Å–µ –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø—Ä–æ–π–¥–µ–Ω—ã —É—Å–ø–µ—à–Ω–æ!")
